package onlineVideo;

public class SelectionDemo {

	public static void main(String args[]) {
		char a = 2;
		switch (a) {
		case 1:
			System.out.println("its one");
			break;
		case 2:
			System.out.println("its two");
			break;
		default:
			System.out.println("nothing");
			break;
		}
		
	}
	
}
